CREATE TABLE sales (
    acct_num VARCHAR(50),
    customer_name VARCHAR(100),
    pay_structure VARCHAR(50),
    part_number VARCHAR(50),
    qty FLOAT,
    amount DECIMAL(18,2),
    product_category VARCHAR(50),
    distributor VARCHAR(100),
    credit_type VARCHAR(50),
    rep VARCHAR(100),
    credit_month INT,
    credit_year INT,
    rep_role_key VARCHAR(100)
);


